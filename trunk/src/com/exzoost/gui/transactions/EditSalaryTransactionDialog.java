    private void SetUpDialog() {
        int row = table.getSelectedRow();
        
        InvoiceTF.setText( (String)table.getModel().getValueAt(row,0) );
        originalinvoice = InvoiceTF.getText();
        try {
            SalaryDateChooser.setDate( df.parse( (String)table.getModel().getValueAt(row,1) ) );
        }
        catch( java.text.ParseException e ) {
            System.out.println(e.getMessage());
        }
        CommentTA.setText( (String)table.getModel().getValueAt(row,2) );
        
        tr.InitializeSalaryEditTransactionDialog( InvoiceTF.getText(), SalaryPaymentTB );
    }
    
