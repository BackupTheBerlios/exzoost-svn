    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
        //get the information from the dialog
        String invoice = InvoiceTF.getText().trim();
        String comment = CommentTA.getText();
        
        boolean result = tr.EditSalaryTransaction(originalinvoice, invoice, 
                new java.sql.Date(SalaryDateChooser.getDate().getTime()), comment );
        
        //error editing salary payment transaction
        if(!result)
            JOptionPane.showMessageDialog(null,"Error editing salary payment transaction data! " +
                    "See log file for detail!", "Warning", JOptionPane.WARNING_MESSAGE );
                
        dispose();
    }//GEN-LAST:event_OKActionPerformed

